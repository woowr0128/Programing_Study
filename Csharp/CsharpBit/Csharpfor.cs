using System;

namespace CsharpBit
{
    class Csharpfor
    {
        static void Main(string[] args)
        {
            int count = 0;
            while (count < 5)
            {
                Console.WriteLine("Hellow World");
            }
            //for (초기화식; 조건식; 반복식)

            for (int i = 0; i < 5; i++)
            {
                Console.WriteLine("Hellow World");
            }
        }
    }
}
